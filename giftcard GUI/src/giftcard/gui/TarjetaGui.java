    private void hacerCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hacerCompraActionPerformed

        Validaciones v = new Validaciones();

        String usuarioT = txtUsuario.getText();
        String totalT = txtTotal.getText();
        String claveT = txtClave.getText();
        boolean f = v.validarTres(usuarioT, claveT, totalT);
        if (f == true) {

            if (usuarioT.equals("RUN sin DV") || usuarioT.equals("")) {
                usuarioT = "0";
            }
            if (totalT.equals("")) {
                totalT = "0";
            }

            if (claveT.equals("")) {
                claveT = "0";
            }

            int usuario = Integer.parseInt(usuarioT);
            int clave1 = (Integer.parseInt(usuarioT)) / 10000;
            int clave = Integer.parseInt(claveT);

            int total = Integer.parseInt(totalT);

            boolean u = v.validarNombre(usuarioT);
            boolean m1 = v.validarMonto2(total);

            boolean t = v.validarUsuario(clave, clave1);

            if (t == true && m1 == true && u == true) {

                for (int contador = 0; contador < listaT.size(); contador++) {

                    if (listaT.get(contador).run == usuario) {
                        int b = listaT.get(contador).monto - total;
                        boolean m = v.validarMonto(b);
                        if  (m == true){
                            tabla.removeRow(contador);

                                b = listaT.get(contador).monto - total;
                                listaT.get(contador).monto = b;

                                Object[] datos = {listaT.get(contador).codigo, listaT.get(contador).run, listaT.get(contador).clave,
                                    listaT.get(contador).monto, listaT.get(contador).vigencia, listaT.get(contador).nombre};
                                tabla.addRow(datos);

                                txtFinal.setText(Integer.toString(listaT.get(contador).monto));
                            }
                        }

                    }

                }

            }

    }//GEN-LAST:event_hacerCompraActionPerformed

